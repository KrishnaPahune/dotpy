num = int(input("Enter a number: "))
if(0<=num<10):
    print("Interval is 0 to 10")
elif(10<=num<20):
    print("Interval is 10 to 20")
elif(20<=num<30):
    print("Interval is 20 to 30")
elif(30<=num<40):
    print("Interval is 30 to 40")
elif(40<=num<50):
    print("Interval is 40 to 50")
else:
    print("Interval is 50 to infinite")
